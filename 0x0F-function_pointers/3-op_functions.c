#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - a function to add to two numbers
 * @a: num1
 * @b: num2
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function to subtract two numbers
 * @a: num1
 * @b: num2
 *
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function to multipy two numbers
 * @a: num1
 * @b: num2
 *
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function to divide two numbers
 * @a: num1
 * @b: num2
 *
 * Return: the sum of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function to find modulus of two numbers
 * @a: num1
 * @b: num2
 *
 * Return: the sum of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
